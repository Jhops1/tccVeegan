            <h1>Dicas</h1>
            <p>
                Com isso, estabelece que a pessoa que pratica o veganismo – vegano ou vegana-, tem motivações muito além de exclusivamente uma dieta alimentar. Portanto, pode-se tratar o veganismo como uma forma de ação social e política, onde seus principais pensamentos estão no contexto ambiental e sustentável.   
            </p>
            <br>
            <h1>1) Começando sua trilhar pelo veganismo</h1>
            <p>
                Comece sendo vegetariano se você ainda come alimentos de origem animal, parar completamente pode ser muito difícil e isso tende a te desmotivar em continuar com o veganismo. Por isso, vá aos poucos.
            </p>
            <p>
                Começando por eliminar a carne, você vai aprender a substituir as proteínas, e essa parte é essencial para uma transição saudável. Além disso, essa vai ser a fase em que você descobre novos sabores e, se ainda não é muito fã de cozinha, vai aprender a se virar muito mais.
            </p>
            <br>
            <h1>2) Utensílios</h1>
            <p>Use colher de bambu, seu canudo de inox ou seu saquinho de pano para produtos a granel, copo de silicone todos esses itens são importantes ter contigo quando for a algum compromisso. Evitando o uso de descartáveis e serve de boicote a políticas de empresas que degradam o meio ambiente e exploram animais.  
            </p>
            <br>
            <h1>3) Medicamentos</h1>
            <p>
                Vale ressaltar, que as vacinas e alguns medicamentos são testados e feitos com produtos animais. Assim, os praticantes do veganismo não têm muito para onde correr. Entretanto, não é considerado uma violação da ética vegana consumir esses remédios e vacinas. O veganismo é vivido na medida do possível e praticável.
            </p>
            <br>
            <h1>4) Troque suas fontes de proteínas</h1>
            <p>
                Use novas fontes de proteína vegetal mesmo que você não queira levar o veganismo à risca, é possível se beneficiar de outras fontes de proteína que não seja a animal, nem que seja apenas para diversificar sua alimentação.
            </p>
            <p>
                Ao comer carnes e laticínios, você acaba ingerindo muita gordura saturada e isso pode ser prejudicial à saúde. Existem fontes vegetais riquíssimas em proteínas, como as lentilhas, feijão, grão-de-bico, tofu, castanhas e sementes em geral.
            </p>
            <p>
                Estes são todos alimentos que comemos no dia a dia e, aumentando um pouquinho a dose, podemos ter toda a proteína necessária ao nosso organismo apenas com elas.
            </p>
            <br>
            <h1>5) Adapte seus pratos favoritos</h1>
            <p>
                Escolha dois ou três pratos favoritos, daqueles que você come várias vezes por semana. Depois, pense e pesquise como você poderia substituir os ingredientes necessários para veganizá-la. Desta forma, resultará mais simples fazer a transição, começando com comidas que você gosta e que já fazem parte do seu cotidiano.
            </p>
            <p>
                O leite de vaca pode ser substituído por leites vegetais como de coco, de amêndoas, de castanha de caju, do pará, de arroz e de aveia. No lugar do ovo, o gel de linhaça ou chia é a opção perfeita.
            </p>
            <br>
            <h1>6) Comida Vegana que cabe no seu bolso</h1>
            <p>
                Algumas pessoas ainda duvidam em abraçar o veganismo por acreditar que este estilo de vida não “caberá no seu bolso”. Embora alguns produtos específicos, como carnes e queijos veganos, possam ter preços mais altos, a alimentação vegana não precisa ser a base desses itens.
            </p>
            <p>
                Os feijões, o grão-de-bico, a lentilha e várias outras leguminosas são ótimas fontes de proteínas econômicas. Além disso, você pode dar preferência a frutas e verduras que estão na época e têm preços mais acessíveis.
            </p>
            <p>
                Com um bom planejamento você pode comer muito bem ter as proteínas necessárias e as vezes até economizar sendo vegano.
            </p>
            <br>
            <h1>7) Chocolate Vegano </h1>
            <p>
                Uma dúvida que pode passar na cabeça de quem está em transição para o veganismo é se existem chocolates veganos. E a resposta é sim! Eles geralmente têm mais cacau ou são mais amargos.
            </p>
            <br>
            <h1>8) Elimine as carnes aos poucos</h1>
            <p>
                Ainda que seja importante dimensionar qual a influência dos produtos de origem animal na sua dieta atual, eliminar tudo de uma vez talvez não seja a decisão mais acertada. É comum que, num primeiro impulso, você queira excluir de cara os alimentos de origem animal da sua dieta. Porém, é mais interessante que você foque sua atenção em adicionar alimentos vegetais que ainda não fazem parte da sua dieta para posteriormente retirar os alimentos de origem animal.
            </p>
            <br>
            <h1>9) Comer fora </h1>
            <p>
                Procure restaurantes veganos e vegetarianos. Para a maioria dos veganos, as melhores opções de comida vêm de restaurantes que servem para a dieta específica deles. Diferente dos estabelecimentos tradicionais, esses restaurantes usam opções veganas como padrão ou têm ingredientes e o conhecimento necessário para fazer substituições deliciosas.
            </p>
            <br>
            <h1>10) Se mantenha forte e não desista</h1>
            <p>
                Como dito nas outras dicas sempre faça uma transição lenta ninguém se tornou vegano do dia para a noite. Fazer uma transição lenta e consciente é essencial para manter a saúde e para que essa escolha seja duradoura na sua vida. Se hoje você não resistiu ao pedaço de queijo, respire fundo e prometa que amanhã você se esforçará mais. Ficar se culpando por eventuais “recaídas” é inútil. Mudar de regime alimentar não é fácil e requer perseverança. Se não deu para ser 100% vegano hoje, haverá sempre a possibilidade de recomeçar tudo amanhã. O importante é não desistir.
            </p>
            
